package com.maddy;

public class Address {
	
	String country;
	String city;
	String streetName;
	String postCode;
	int doorNumber;
	
	public Address(String country, String city, String streetName, String postCode, int doorNumber) {
		
		this.country = country;
		this.city = city;
		this.streetName = streetName;
		this.postCode = postCode;
		this.doorNumber = doorNumber;
				
	}

}
